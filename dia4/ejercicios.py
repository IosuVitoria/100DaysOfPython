# 4. Intercambia el valor de dos variables.

# Valores iniciales
a = 10
b = 20

# Mostrar antes del intercambio
print("Antes:")
print("a =", a)
print("b =", b)

# Intercambio
a, b = b, a

# Mostrar después del intercambio
print("\nDespués:")
print("a =", a)
print("b =", b)

# Ejercicio extra con 3 variables:

# Valores iniciales
x = 1
y = 2
z = 3

# Mostrar antes del intercambio
print("Antes:")
print("x =", x)
print("y =", y)
print("z =", z)

# Intercambio
x, y, z = y, z, x

# Mostrar después
print("\nDespués:")
print("x =", x)
print("y =", y)
print("z =", z)